#include "./libft.h"

void	ft_putlong(long nb)
{
	if (nb % 10 + '0' == '(')
	{
		ft_putstr("9223372036854775808");
		return ;
	}
	if (nb / 10 > 0)
		ft_putlong(nb / 10);
	ft_putchar(nb % 10 + '0');
}
